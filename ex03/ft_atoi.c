int	ft_atoi(char *str)
{
	long	l;
	int		sign;

	l = 0;
	sign = 1;
	while (*str == ' ' || *str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		l = (l * 10) + (*str - '0');
		str++;
	}
	return (l * sign);
}
