#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	long	i;

	i = (long)nb;
	if (nb < 0)
	{
		write(1, "-", 1);
		i = 0 - i;
	}
	if (i >= 10)
		ft_putnbr(i / 10);
	c = (i % 10) + '0';
	write (1, &c, 1);
}
